  Future<String> _loginUser(LoginData data) {
    return Future.delayed(loginTime).then((_) {
      if (!mockUsers.containsKey(data.name)) {
        return '不存在该用户';
      }
      if (mockUsers[data.name] != data.password) {
        return '密码不正确';
      }
      return null;
    });
  }

